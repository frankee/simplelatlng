package com.javadocmd.simplelatlng.chinatool;

final class Point {
	private double x;
    private double y;
    
    void setX(double x) {
        this.x = x;
    }
    
    double getX() {
        return this.x;
    }
    
    void setY(double y) {
        this.y = y;
    }
    
    double getY() {
        return this.y;
    }
}
